
public class VariableTest {

	public static void main(String[] args) {
		char myChar='\u0041'; //A
		System.out.println("myChar = "+ myChar);
		
		int myInt=0x15; //21
		System.out.println("myInt = "+ myInt);
		
		float myFloat=15.1F;
		System.out.println("myFloat = "+ myFloat);
		
		System.out.println("Single Quote(\')\tBackslash(\\)");
	}

}
